public class whileloopex1 {
        public static void main(String[] args) {
            int daysUntilVacation = 5;
    
            System.out.println("Vacation countdown:");
            while (daysUntilVacation > 0) {
                System.out.println("Only " + daysUntilVacation + " days left!");
                daysUntilVacation--;
            }
    
            System.out.println("It's vacation time!");
        }
    }
    
    

